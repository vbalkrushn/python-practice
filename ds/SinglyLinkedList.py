class Node:
    def __init__(self, value):
        self.value = value
        self.next = None


class LinkedList:
    def __init__(self):
        self.head = None

    def append(self, value):
        if not self.head:
            self.head = Node(value)
        else:
            current = self.head
            while current.next:
                current = current.next
            current.next = Node(value)

    def __str__(self):
        values = []
        current = self.head
        while current:
            values.append(str(current.value))
            current = current.next
        return ' -> '.join(values)


if __name__ == '__main__':
    linkedlist = LinkedList()
    linkedlist.append(10)
    linkedlist.append(21)
    linkedlist.append(7)
    linkedlist.append(20)

    print(linkedlist)